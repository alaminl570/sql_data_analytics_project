/*
===============================================================================
CUSTOMER PERFORMANCE & BEHAVIOR REPORT
===============================================================================
Purpose:
    This query generates a consolidated customer-level report that:

        • Aggregates transactional data per customer
        • Calculates lifecycle and behavioural KPIs
        • Segments customers by value and longevity
        • Categorises customers into age groups

    The output supports CRM strategy, retention analysis,
    and revenue performance evaluation.
===============================================================================
*/


-- ==========================================================
-- 1) Base Layer: Prepare transaction-level customer dataset
-- ==========================================================
WITH base_query AS (
    SELECT
        s.order_number,
        s.product_key,
        s.order_date,
        s.sales_amount,
        s.quantity,

        c.customer_key,
        c.customer_number,
        CONCAT(c.first_name, ' ', c.last_name) AS customer_name,

        -- Approximate age based on current date
        DATEDIFF(year, c.birthdate, GETDATE()) AS age

    FROM sales s
    LEFT JOIN customers c
        ON c.customer_key = s.customer_key
    WHERE s.order_date IS NOT NULL   -- Ensures valid time-based metrics
)


-- ==========================================================
-- 2) Aggregation Layer: Compute customer-level metrics
-- ==========================================================
, customer_aggregation AS (
    SELECT 
        customer_key,
        customer_number,
        customer_name,
        age,

        COUNT(DISTINCT order_number) AS total_orders,
        SUM(sales_amount) AS total_sales,
        SUM(quantity) AS total_quantity,
        COUNT(DISTINCT product_key) AS total_products,

        MAX(order_date) AS last_order_date,

        -- Customer active duration in months
        DATEDIFF(month, MIN(order_date), MAX(order_date)) AS lifespan

    FROM base_query
    GROUP BY 
        customer_key,
        customer_number,
        customer_name,
        age
)


-- ==========================================================
-- 3) Final Reporting Layer: Segmentation + KPI Calculation
-- ==========================================================
SELECT
    customer_key,
    customer_number,
    customer_name,
    age,

    -- Age group classification
    CASE 
        WHEN age < 20 THEN 'Under 20'
        WHEN age BETWEEN 20 AND 29 THEN '20-29'
        WHEN age BETWEEN 30 AND 39 THEN '30-39'
        WHEN age BETWEEN 40 AND 49 THEN '40-49'
        ELSE '50 and above'
    END AS age_group,

    -- Value-based segmentation
    CASE 
        WHEN lifespan >= 12 AND total_sales > 5000 THEN 'VIP'
        WHEN lifespan >= 12 AND total_sales <= 5000 THEN 'Regular'
        ELSE 'New'
    END AS customer_segment,

    last_order_date,

    -- Months since last purchase
    DATEDIFF(month, last_order_date, GETDATE()) AS recency,

    total_orders,
    total_sales,
    total_quantity,
    total_products,
    lifespan,

    -- Average Order Value (AOV)
    CASE 
        WHEN total_orders = 0 THEN 0
        ELSE total_sales / total_orders
    END AS avg_order_value,

    -- Average Monthly Spend
    CASE 
        WHEN lifespan = 0 THEN total_sales
        ELSE total_sales / lifespan
    END AS avg_monthly_spend

FROM customer_aggregation;
